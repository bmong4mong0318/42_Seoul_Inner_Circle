#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long	nn;

	nn = n;
	if (nn < 0)
	{
		write(fd, "-", 1);
		nn *= -1;
	}
	if (nn > 9)
		ft_putnbr_fd(nn / 10, fd);
	write (fd, &"0123456789"[nn % 10], 1);
}

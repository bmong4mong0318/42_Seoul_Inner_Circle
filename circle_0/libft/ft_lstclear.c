#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		tmp = (*lst)->next;
		(*lst)->next = NULL;
		ft_lstdelone(*lst, del);
		*lst = tmp;
	}
}
